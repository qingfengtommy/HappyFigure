def collect_generate_artifacts(
    run_dir: str,
    mode: str,
    experiments: list[str],
) -> dict[str, str]:
    artifacts: dict[str, str] = {}
    if mode == "exp_plot":
        for exp in experiments:
            fig = os.path.join("outputs", exp, "figure.png")
            if os.path.exists(os.path.join(run_dir, fig)):
                artifacts[ArtifactKeys.figure(exp)] = fig
            # Critic result lives in workspace (experiments/<exp>/), not outputs/
            ws_crit = os.path.join(
                orch_art.EXPERIMENTS_DIR,
                exp,
                orch_art.CRITIC_RESULT,
            )
            if os.path.exists(os.path.join(run_dir, ws_crit)):
                artifacts[f"critic_workspace/{exp}"] = ws_crit
            # Backward compat: check old location in outputs/<exp>/
            elif os.path.exists(os.path.join(run_dir, "outputs", exp, orch_art.CRITIC_RESULT)):
                artifacts[f"critic_workspace/{exp}"] = os.path.join("outputs", exp, orch_art.CRITIC_RESULT)
        # Composite paper figures
        pf_dir = os.path.join(run_dir, "outputs", orch_art.PAPER_FIGURES_DIR)
        if os.path.isdir(pf_dir):
            for fname in sorted(os.listdir(pf_dir)):
                if fname.endswith((".png", ".pdf")):
                    rel = os.path.join("outputs", orch_art.PAPER_FIGURES_DIR, fname)
                    artifacts[f"paper_figure/{fname}"] = rel
    else:
        for name in ("method_architecture.svg", "method_architecture.png"):
            if os.path.exists(os.path.join(run_dir, name)):
                artifacts[name] = name
        composed = "method_architecture_composed.png"
        if os.path.exists(os.path.join(run_dir, composed)):
            artifacts["composed"] = composed
    return artifacts


def stage_metadata(
    run_dir: str,
    mode: str,
    args: argparse.Namespace,
    *,
    stage: str,
    design: DesignResult | None = None,
) -> dict[str, object]:
    metadata: dict[str, object] = {
        "mode": mode,
        "stage": stage,
        "services_required": mode == "composite" and stage == "generate",
    }

    if mode == "exp_plot":
        metadata["execution_strategy"] = getattr(args, "execution", "sequential")

    if design is not None:
        metadata["experiment_count"] = len(design.experiments)
        if mode == "exp_plot" and design.experiments:
            metadata["experiment_artifacts"] = {
                exp: orch_art.plot_experiment_index_entry(
                    run_dir,
                    exp,
                    beam_variant_specs=(design.variant_specs or {}).get(exp),
                )
                for exp in design.experiments
            }
        if design.variant_specs:
            metadata["beam_variant_experiments"] = len(design.variant_specs)

    if mode == "composite" and stage == "generate":
        metadata["skip_viz_compose"] = bool(getattr(args, "skip_viz_compose", False))

    return metadata


def stage_generate(
    run_dir: str,
    args: argparse.Namespace,
    mode: str,
    design: DesignResult,
) -> None:
    execute_generate_strategy(run_dir, args, mode, design)

    if mode == "composite" and getattr(args, "skip_viz_compose", False):
        ui.dim("Skipping viz-composer (--skip-viz-compose)")

    gen_artifacts = collect_generate_artifacts(run_dir, mode, design.experiments)
    write_manifest_stage(
        run_dir,
        "generate",
        StageRecord(
            status=StageStatus.COMPLETED,
            completed_at=datetime.datetime.now().isoformat(),
            artifacts=gen_artifacts,
            experiments=design.experiments,
            metadata=stage_metadata(run_dir, mode, args, stage="generate", design=design),
        ),
    )


def try_resume(run_dir: str, mode: str) -> tuple[ExplorationResult | None, DesignResult | None]:
    explore_rec = read_manifest_stage(run_dir, "explore")
    design_rec = read_manifest_stage(run_dir, "design")

    exploration: ExplorationResult | None = None
    design: DesignResult | None = None

    if explore_rec and explore_rec.status == StageStatus.COMPLETED:
        exploration = ExplorationResult(
            run_dir=run_dir,
            mode=mode,
            artifacts=explore_rec.artifacts,
            experiments=explore_rec.experiments,
        )

    if design_rec and design_rec.status == StageStatus.COMPLETED:
        variant_specs = None
        vs_rel = design_rec.artifacts.get("beam_variant_specs")
        if vs_rel:
            vs_path = os.path.join(run_dir, vs_rel)
            if os.path.exists(vs_path):
                with open(vs_path, encoding="utf-8") as f:
                    variant_specs = json.load(f)
        elif mode == "exp_plot":
            experiment_artifacts = design_rec.metadata.get("experiment_artifacts", {})
            extracted_variants = {
                exp: data.get("beam_variant_specs", [])
                for exp, data in experiment_artifacts.items()
                if isinstance(data, dict) and data.get("beam_variant_specs")
            }
            if extracted_variants:
                variant_specs = extracted_variants

        design = DesignResult(
            mode=mode,
            artifacts=design_rec.artifacts,
            experiments=design_rec.experiments,
            variant_specs=variant_specs,
        )

    return exploration, design
